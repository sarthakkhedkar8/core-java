public class problem4boxofstar {
    public static void main (String[] args){
        int i;
        // Print 5 asterisks using a for loop   
        for(i=0 ; i<5 ; i++){
            System.out.println(' ');
            for(int j=0 ; j<5 ; j++){
                System.out.print('*');
            }
        }
    }
}
